public class Chapter6 {
    public static void main(String[] args) {
        System.out.println(myAtoi(" -1010023630o4"));
    }

    public static int myAtoi(String str) {
        int maxInt = Integer.MAX_VALUE;
        int minInt = Integer.MIN_VALUE;

        if (str.length() < 1) {
            return 0;
        }

        int index = 0;
        while (index < str.length() && Character.isSpaceChar(str.charAt(index))) {
            index++;
        }
        str = str.substring(index);

        StringBuffer sb = new StringBuffer();
        boolean isDigital = false;
        for (int i = 0; i < str.length(); i++) {
            char ch = str.charAt(i);
            if (i == 0 && (ch == '-' || ch == '+')) {
                sb.append(ch);
            } else if (!Character.isDigit(ch)) {
                break;
            }
            if (sb.length() > 8) {
                int value = Integer.valueOf(sb.toString());
                if (value > maxInt / 10 || (value == maxInt / 10 && ch > '7')) {
                    return maxInt;
                }
                if (value < minInt / 10 || (value == minInt / 10 && ch > '8')) {
                    return minInt;
                }
            }
            if (Character.isDigit(ch)) {
                sb.append(ch);
                isDigital = true;
            }
        }
        if (!isDigital) {
            return 0;
        }
        return Integer.valueOf(sb.toString());
    }
}


class Solution {
    public int myAtoi(String str) {
        Automaton automaton = new Automaton();
        int length = str.length();
        for (int i = 0; i < length; ++i) {
            automaton.get(str.charAt(i));
        }
        return (int) (automaton.sign * automaton.ans);
    }
}
class Automaton {
    public int sign = 1;
    public long ans = 0;
    private String state = "start";
    private Map<String, String[]> table = new HashMap<String, String[]>() {{
        put("start", new String[]{"start", "signed", "in_number", "end"});
        put("signed", new String[]{"end", "end", "in_number", "end"});
        put("in_number", new String[]{"end", "end", "in_number", "end"});
        put("end", new String[]{"end", "end", "end", "end"});
    }};
    public void get(char c) {
        state = table.get(state)[get_col(c)];
        if ("in_number".equals(state)) {
            ans = ans * 10 + c - '0';
            ans = sign == 1 ? Math.min(ans, (long) Integer.MAX_VALUE) : Math.min(ans, -(long) Integer.MIN_VALUE);
        } else if ("signed".equals(state)) {
            sign = c == '+' ? 1 : -1;
        }
    }
    private int get_col(char c) {
        if (c == ' ') {
            return 0;
        }
        if (c == '+' || c == '-') {
            return 1;
        }
        if (Character.isDigit(c)) {
            return 2;
        }
        return 3;
    }
}